    private String seleccionarPlan(){
        String tipoPlan = null;
        if(jrbPlanSalvaje.isSelected()){
            tipoPlan = "Plan Salvaje";
        }else if(jrbPlanMariposario.isSelected()){
            tipoPlan = "Plan Mariposario";
        }else if(jrbPlanVenenoso.isSelected()){
            tipoPlan = "Plan Venenoso";
        }else if(jrbPlanGranja.isSelected()){
            tipoPlan = "Plan Granja";
        }else if(jrbPlanAcuario.isSelected()){
            tipoPlan = "Plan Acuario";
        }
        
        return tipoPlan;
    }
    
